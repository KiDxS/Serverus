function editBtnClicked() {
    document.getElementById("customer_name").disabled = false;
    document.getElementById("save_button").disabled = false;
    document.getElementById("address").disabled = false;
    document.getElementById("phone_number").disabled = false;
    document.getElementById("customer_name").style.color = "black";
    document.getElementById("address").style.color = "black";
    document.getElementById("phone_number").style.color = "black";
}
